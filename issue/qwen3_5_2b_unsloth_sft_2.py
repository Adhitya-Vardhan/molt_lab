@dataclass
class ResponseOnlyCollator:
    tokenizer: object
    response_template_ids: list
    debug_printed: bool = False

    def __call__(self, features):
        # Pad the batch
        batch = self.tokenizer.pad(features, return_tensors="pt", padding=True)
        labels = batch["input_ids"].clone()

        # For each sample, mask everything BEFORE the assistant response
        resp = self.response_template_ids
        for i in range(len(labels)):
            ids = batch["input_ids"][i].tolist()
            # Search for the response template pattern
            found = -1
            for j in range(len(ids) - len(resp) + 1):
                if ids[j:j+len(resp)] == resp:
                    found = j + len(resp)  # mask up to end of "<|im_start|>assistant\n"
                    break
            if found != -1:
                labels[i, :found] = -100
            else:
                preview = self.tokenizer.decode(ids[:300])
                raise ValueError(
                    "Assistant response marker was not found; refusing to train on an all-masked batch. "
                    f"Decoded preview: {preview!r}"
                )
            # Also mask padding tokens
            labels[i, batch["attention_mask"][i] == 0] = -100

            supervised = labels[i][labels[i] != -100]
            if supervised.numel() == 0:
                raise ValueError("No supervised assistant tokens found in this sample.")
            if not self.debug_printed:
                print("SUPERVISED TARGET PREVIEW:")
                print(self.tokenizer.decode(supervised[:500]))
                self.debug_printed = True

        batch["labels"] = labels
        return batch

